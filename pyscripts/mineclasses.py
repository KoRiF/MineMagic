class MineTask():
    def __init_(self, mc):
        self.mc = mc
    def do(self):
        pass
    def __call__(self):
        do()

class MineDaemon(MineTask):
    def __init__(self, mc):
        super().__init__(mc)
        self._alive = True
    def do_step():
        pass
    def __call__(self):
        do_step()
        return self
        